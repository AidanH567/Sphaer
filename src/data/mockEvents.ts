/**
 * Mock event data for the Feed and Event Detail screens.
 *
 * This file is the single source of truth for dummy events while the
 * Supabase `events` table is empty. Each object is shaped as a real
 * `EventWithRelations` (the same type the Supabase queries return), so the
 * UI can be swapped to live data later with no component changes:
 *
 *   - Feed:   replace `MOCK_EVENTS` with `useEvents()`        (events.service.ts)
 *   - Detail: replace `getMockEventById()` with `getEventById()` (events.service.ts)
 *
 * The only non-DB fields are `priceLabel` (a display-only price range that the
 * single numeric `price` column can't express) and `hostStats` (follower /
 * activity counts that live on other tables in the real schema).
 */
import type { EventWithRelations } from '@/types/event.types';
import type { Profile } from '@/types/user.types';

export interface MockEvent extends EventWithRelations {
  /** Display-only price override, e.g. "80€ - 250€". Falls back to `price` when absent. */
  priceLabel?: string;
  /** Host metadata not stored on the `profiles` row (counts live on other tables). */
  hostStats: {
    activities: number;
    followers: number;
    verified: boolean;
  };
  /**
   * Pre-measured poster image dimensions (pixels). Only set on figma-seed
   * events so the Mural can lay out without an Image.getSize() round-trip
   * — the values were captured at upload time by `import-figma-posters.ts`.
   * Forward-compatible with the planned `poster_width`/`poster_height`
   * columns on the events table (see BACKLOG).
   */
  poster_width?: number;
  poster_height?: number;
}

